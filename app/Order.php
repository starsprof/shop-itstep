 */
class Order extends Model
{
    public const WAIT = 'Ожидание';
    public const PAYMENT = 'Оплачено';
    public const DELIVERY = 'Доставка';
    public const DONE = 'Выполнено';

    public function user()
    {
        return $this->belongsTo(User::class);
    }

    protected $fillable = [
        'address'
    ];

    protected $casts = [
        'products' => 'array',
    ];
}
